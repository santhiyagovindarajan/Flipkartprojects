class Non_Static_Variables
{
byte b; 
short s; 
int i; 
long l; 
float f; 
double d; 
boolean bo; 
char c; 
public static void main(String[] args)
{
Non_Static_Variables nsv = new Non_Static_Variables(); 
System.out.println("byte " + nsv.b); 
System.out.println("short " + nsv.s); 
System.out.println("int " + nsv.i); 
System.out.println("long " + nsv.l); 
System.out.println("float " + nsv.f); 
System.out.println("double " + nsv.d); 
System.out.println("boolean " + nsv.bo); 
System.out.println("char " + nsv.c); 
}


}
